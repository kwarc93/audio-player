const char *FB_GetFileExtension(char *filename)
{
    char *dot = strrchr(filename, '.');
    if(!dot || dot == filename) return "";
    dot++;
    // Convert to lowercase
    for(uint8_t i = 0; dot[i]; i++)
    {
    	dot[i] = tolower(dot[i]);
    }
    return dot;
}
